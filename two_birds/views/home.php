                    Además, si bien la carne puede ser una buena fuente de proteínas, vitaminas, calcio, etc. <br>Estos pueden ser reemplazados por una dieta a base de vegetales que aporte lo mismo <br>aunque tiene que hacerse correctamente esto supondria de una reducion de 0.01 y 1.5 toneladas.
